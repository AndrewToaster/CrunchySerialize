namespace CrunchySerialize
{
    /// <summary>
    /// Enumeration class containing integral types
    /// </summary>
    public enum IntegralTypes : byte
    {
        Int = SerializableTypes.Int,
        UInt = SerializableTypes.UInt,
        Long = SerializableTypes.Long,
        ULong = SerializableTypes.ULong,
        Short = SerializableTypes.Short,
        UShort = SerializableTypes.UShort,
        Byte = SerializableTypes.Byte
    }
}
